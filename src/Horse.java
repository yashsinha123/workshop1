public class Horse implements Animal {
    @Override
    public void breath() {
        System.out.println("horse breath");
    }

    @Override
    public void eat() {
        System.out.println("horse eat");

    }

    @Override
    public void sleep() {
        System.out.println("horse sleep");

    }

    @Override
    public void move() {
        System.out.println("horse move");

    }
}
