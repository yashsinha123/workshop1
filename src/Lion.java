public class Lion implements Animal {

    @Override
    public void breath() {
        System.out.println("lion is breathing okay");
    }

    @Override
    public void eat() {
        System.out.println("lion eats");

    }

    @Override
    public void sleep() {
        System.out.println("lion sleeping");

    }

    @Override
    public void move() {
        System.out.println("lion moves");

    }
}
